// Require Mongoose
const mongoose = require("mongoose");

// Define a schema
const Schema = mongoose.Schema;

const CitySchema = new Schema({
    city:{
        type: String,
        required: true,
    },
    population:{
        type: String,
        required: true,
    },
    country:{
        type: String,
        required:true,
    },
    skyscrapers:{
        type: String,
        required:true,
    },
    date:{
        type: Date,
        required: true,
        default: Date.now()
    }

},{toJSON: {virtuals: true}
})
//Add virtual property to City, to include dynamic links
CitySchema.virtual('_links').get(
    function () {
        return {
            self: {
                href: `${process.env.BASE_URI}cities/${this._id}`
            },
            collection: {
                href: `${process.env.BASE_URI}cities/`
            }
        }
    }
);

// Export function to create "SomeModel" model class
module.exports = mongoose.model("City", CitySchema);






































































































































































































































































































































































































































































